
# prompt direction
direction = input("Towards which direction should I go (up, down, left, right): ")

# evaluate the direction
if direction == "up":
    print("I am moving in an upward direction!")
elif direction == "down":
    print("I am moving in a downward direction!")
elif direction == "left":
    print("I am moving in a leftward direction!")
elif direction == "right":
    print("I am moving in a rightward direction!")
else:
    print("Invalid direction! Not sure which direction to move!")